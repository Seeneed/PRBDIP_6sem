CREATE TABLE Roles (
    RoleID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RoleName VARCHAR2(50) NOT NULL UNIQUE
);

CREATE TABLE OrderStatus (
    StatusID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    StatusName VARCHAR2(50) NOT NULL
);

CREATE TABLE Users (
    UserID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RoleID NUMBER REFERENCES Roles(RoleID),
    FullName VARCHAR2(150) NOT NULL,
    Email VARCHAR2(100) UNIQUE,
    Phone VARCHAR2(50),
    PasswordHash VARCHAR2(255) NOT NULL
);

CREATE TABLE Vehicles (
    VehicleID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DriverID NUMBER REFERENCES Users(UserID),
    Brand VARCHAR2(100),
    LicensePlate VARCHAR2(20) UNIQUE,
    CapacityKG NUMBER(10,2),
    VolumeM3 NUMBER(10,2)
);

CREATE TABLE Orders (
    OrderID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ClientID NUMBER REFERENCES Users(UserID),
    VehicleID NUMBER REFERENCES Vehicles(VehicleID),
    StatusID NUMBER REFERENCES OrderStatus(StatusID),
    PickupAddress VARCHAR2(255) NOT NULL,
    DropAddress VARCHAR2(255) NOT NULL,
    Price NUMBER(18,2) DEFAULT 0,
    OrderDate DATE DEFAULT SYSDATE
);

DROP TABLE Orders CASCADE CONSTRAINTS;
DROP TABLE Vehicles CASCADE CONSTRAINTS;
DROP TABLE Users CASCADE CONSTRAINTS;
DROP TABLE OrderStatus CASCADE CONSTRAINTS;
DROP TABLE Roles CASCADE CONSTRAINTS;