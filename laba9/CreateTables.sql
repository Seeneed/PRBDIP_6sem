CREATE TABLE Roles (
    RoleID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RoleName VARCHAR2(50) NOT NULL UNIQUE
);

CREATE TABLE Users (
    UserID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RoleID NUMBER REFERENCES Roles(RoleID),
    FullName VARCHAR2(150) NOT NULL,
    Email VARCHAR2(100) UNIQUE
);

CREATE TABLE LegalEntities (
    EntityID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EntityName VARCHAR2(150) NOT NULL
);

CREATE TABLE Departments (
    DeptID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EntityID NUMBER REFERENCES LegalEntities(EntityID),
    DeptName VARCHAR2(100) NOT NULL
);

CREATE TABLE Vacancies (
    VacancyID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DeptID NUMBER REFERENCES Departments(DeptID),
    Title VARCHAR2(100) NOT NULL
);

CREATE TABLE AppStatuses (
    StatusID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    StatusName VARCHAR2(50) NOT NULL
);

CREATE TABLE Applications (
    AppID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CandidateID NUMBER REFERENCES Users(UserID),
    VacancyID NUMBER REFERENCES Vacancies(VacancyID),
    HR_ID NUMBER REFERENCES Users(UserID),
    StatusID NUMBER REFERENCES AppStatuses(StatusID),
    ApplyDate DATE NOT NULL,
    ResolutionDate DATE
);

DROP TABLE Applications CASCADE CONSTRAINTS;
DROP TABLE Vacancies CASCADE CONSTRAINTS;
DROP TABLE Departments CASCADE CONSTRAINTS;
DROP TABLE LegalEntities CASCADE CONSTRAINTS;
DROP TABLE AppStatuses CASCADE CONSTRAINTS;
DROP TABLE Users CASCADE CONSTRAINTS;
DROP TABLE Roles CASCADE CONSTRAINTS;