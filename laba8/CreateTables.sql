CREATE TABLE Clients (
    ClientID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ClientName VARCHAR2(150) NOT NULL
);

CREATE TABLE ServiceTypes (
    ServiceID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ServiceName VARCHAR2(100) NOT NULL
);

CREATE TABLE Orders (
    OrderID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ClientID NUMBER REFERENCES Clients(ClientID),
    ServiceID NUMBER REFERENCES ServiceTypes(ServiceID),
    OrderDate DATE NOT NULL,
    Distance NUMBER NOT NULL
);

DROP TABLE Orders CASCADE CONSTRAINTS;
DROP TABLE ServiceTypes CASCADE CONSTRAINTS;
DROP TABLE Clients CASCADE CONSTRAINTS;